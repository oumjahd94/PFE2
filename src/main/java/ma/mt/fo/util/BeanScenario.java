package ma.mt.fo.util;

public class BeanScenario {
	private String idScenario;

	private String valueScenario;

	private String refTypeFonds;

	public String getIdScenario() {
		return idScenario;
	}

	public void setIdScenario(String idScenario) {
		this.idScenario = idScenario;
	}

	public String getValueScenario() {
		return valueScenario;
	}

	public void setValueScenario(String valueScenario) {
		this.valueScenario = valueScenario;
	}

	public String getRefTypeFonds() {
		return refTypeFonds;
	}

	public void setRefTypeFonds(String refTypeFonds) {
		this.refTypeFonds = refTypeFonds;
	}

}
